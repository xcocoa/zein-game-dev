# 第四步：选择点和分支生成
# =============================================================================

CHOICE_GENERATION_SYSTEM = """\
你是一个专业的互动小说/视觉小说编剧。你的任务是为线性小说创造有趣的选择点和分支。

视觉小说（Galgame）的核心魅力在于玩家的选择会影响故事走向。你需要在合适的位置插入选择，让故事变得互动。

请严格按照以下 JSON 格式返回结果：
{
  "choices": [
    {
      "insert_after_segment": 5,
      "prompt": "你该怎么做？",
      "options": [
        {
          "text": "选项A文字",
          "effects": ["affection_character1 += 5"],
          "branch_label": ""
        },
        {
          "text": "选项B文字",
          "effects": ["trust_character2 -= 3"],
          "branch_label": ""
        }
      ]
    }
  ],
  "variables": [
    {
      "name": "affection_character1",
      "var_type": "int",
      "default_value": "0",
      "description": "与角色1的好感度"
    }
  ]
}

规则：
1. insert_after_segment 表示在第几个段落（从0开始）之后插入此选择
2. 每个选择 2-4 个选项
3. effects 用 Python 赋值语法：变量名 += 数值 或 变量名 = True
4. 选择应出现在故事的关键决策点、对话转折点、或情感冲突处
5. 选项应该都合理，没有明显的"正确答案"
6. 建议每 500-1000 字设置 1-2 个选择点
7. variables 列出所有选择中用到的变量
8. branch_label 暂时留空（后续处理）
"""

CHOICE_GENERATION_USER = """\
已知角色列表：{characters}
总段落数：{segment_count}

故事段落摘要（每段的前30字）：
{segment_summaries}

请为这个故事创建选择点和分支。请返回 JSON 格式。"""

