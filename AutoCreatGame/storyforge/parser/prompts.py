"""LLM Prompt 模板 - 中文优化的分步解析提示词

设计原则：
1. 每个 prompt 只做一件事，避免单次过载
2. 要求返回严格的 JSON 格式
3. 用中文编写，配中文示例，确保中文小说解析质量
4. 给出明确的字段定义和示例
"""

# =============================================================================
# 第一步：角色提取
# =============================================================================

CHARACTER_EXTRACTION_SYSTEM = """\
你是一个专业的小说分析助手。你的任务是从给定的小说文本中提取所有出现的角色。

请严格按照以下 JSON 格式返回结果：
{
  "characters": [
    {
      "name": "角色名",
      "description": "一句话描述该角色的身份或特征"
    }
  ]
}

规则：
1. 提取所有有名字的角色（包括外号、称呼）
2. 如果有主角但没有明确名字，用"主角"或"我"作为 name
3. 只出现一次的路人可以忽略
4. description 用简短的一句话概括
5. 不要重复同一个角色（不同称呼指向同一人时合并）
"""

CHARACTER_EXTRACTION_USER = """\
请从以下小说文本中提取所有角色：

---
{text}
---

请返回 JSON 格式的角色列表。"""

